package com.sanzeng.hello_watch.entity;

import com.google.gson.annotations.SerializedName;

import java.io.Serializable;
import java.util.List;

/**
 * 颜色类型
 * Created by YY on 2017/6/14.
 */
public class ColorTypeEntity implements Serializable{

    @SerializedName(value = "result")
    private String result;

    @SerializedName(value = "data")
    private List<PersonColorType> data;

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }

    public List<PersonColorType> getData() {
        return data;
    }

    public void setData(List<PersonColorType> data) {
        this.data = data;
    }
}
