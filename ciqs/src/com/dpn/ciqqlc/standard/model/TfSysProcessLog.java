package com.dpn.ciqqlc.standard.model;

import java.util.Date;

public class TfSysProcessLog {
	private String decl_no;
	
	private String process_node;
	
	private String end_date;
	
	private String trea_oper_name;

	public String getDecl_no() {
		return decl_no;
	}

	public void setDecl_no(String decl_no) {
		this.decl_no = decl_no;
	}

	public String getProcess_node() {
		return process_node;
	}

	public void setProcess_node(String process_node) {
		this.process_node = process_node;
	}

	public String getEnd_date() {
		return end_date;
	}

	public void setEnd_date(String end_date) {
		this.end_date = end_date;
	}

	public String getTrea_oper_name() {
		return trea_oper_name;
	}

	public void setTrea_oper_name(String trea_oper_name) {
		this.trea_oper_name = trea_oper_name;
	}
}
