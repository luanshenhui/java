package cn.rkylin.oms.system.config;

import java.io.Serializable;
import java.util.Map;

/**
 * 页面元素
 */
public class ElementDefine implements Serializable {

	/**
	 * 元素名称
	 */
	private String name;

	/**
	 * 元素类型：TextFiend,DateField,NumberField,ComboBox,
	 */
	private String type;

	/**
	 * 元素对应表中的列名
	 */
	private String column;

	/**
	 * 列的类型
	 */
	private String columnType;

	/**
	 * 元素显示的名称
	 */
	private String label;

	/**
	 * 是否允许为空
	 */
	private String allowBlank;

	/**
	 * 对输入进行验证的正则表达式
	 */
	private String regex;

	/**
	 * 最大长度
	 */
	private String maxLength;

	/**
	 * 验证失败提示信息
	 */
	private String errorInfoText;

	/**
	 * 页面元素的宽度
	 */
	private String width;

	/**
	 * 页面元素是否必读
	 */
	private String readOnly;

	/**
	 * 页面元素值的格式化
	 */
	private String format;

	/**
	 * 页面元素对应的值提取类
	 */
	private String store;

	/**
	 * 用于存储从store中取出的名值对儿
	 */
	private Map storeValueMap;

	/**
	 * 构造函数
	 */
	public ElementDefine() {

	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getColumn() {
		return column;
	}

	public void setColumn(String column) {
		this.column = column;
	}

	public String getColumnType() {
		return columnType;
	}

	public void setColumnType(String columnType) {
		this.columnType = columnType;
	}

	public String getLabel() {
		return label;
	}

	public void setLabel(String label) {
		this.label = label;
	}

	public String getAllowBlank() {
		return allowBlank;
	}

	public void setAllowBlank(String allowBlank) {
		this.allowBlank = allowBlank;
	}

	public String getRegex() {
		return regex;
	}

	public void setRegex(String regex) {
		this.regex = regex;
	}

	public String getMaxLength() {
		return maxLength;
	}

	public void setMaxLength(String maxLength) {
		this.maxLength = maxLength;
	}

	public String getErrorInfoText() {
		return errorInfoText;
	}

	public void setErrorInfoText(String errorInfoText) {
		this.errorInfoText = errorInfoText;
	}

	public String getWidth() {
		return width;
	}

	public void setWidth(String width) {
		this.width = width;
	}

	public String getReadOnly() {
		return readOnly;
	}

	public void setReadOnly(String readOnly) {
		this.readOnly = readOnly;
	}

	public String getFormat() {
		return format;
	}

	public void setFormat(String format) {
		this.format = format;
	}

	public String getStore() {
		return store;
	}

	public void setStore(String store) {
		this.store = store;
	}

	public void setStoreValueMap(Map storeValueMap) {
		this.storeValueMap = storeValueMap;
	}

	public Map getStoreValueMap() {
		return storeValueMap;
	}

}
