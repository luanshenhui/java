package rcmtm.entity;

public class Packets {
	private String cpcode;// 企业平台代码
	private String loginid;// 企业平台账号，若未绑定，则该字段为空
	private String userid;// 善融帐号
	private String company;//企业中文名称
	private String loginname;// 公司名称
	private String timestamp;// 时间
	private String auth;// 时间戳：格式(yyyyMMddHHmmss)验证字符串
						// md5(cpcode+loginid+userid+company+ timestamp+key)

	public String getCpcode() {
		return cpcode;
	}

	public void setCpcode(String cpcode) {
		this.cpcode = cpcode;
	}

	public String getLoginid() {
		return loginid;
	}

	public void setLoginid(String loginid) {
		this.loginid = loginid;
	}

	public String getUserid() {
		return userid;
	}

	public void setUserid(String userid) {
		this.userid = userid;
	}

	public String getTimestamp() {
		return timestamp;
	}

	public void setTimestamp(String timestamp) {
		this.timestamp = timestamp;
	}

	public String getAuth() {
		return auth;
	}

	public void setAuth(String auth) {
		this.auth = auth;
	}

	public String getLoginname() {
		return loginname;
	}

	public void setLoginname(String loginname) {
		this.loginname = loginname;
	}

	public String getCompany() {
		return company;
	}

	public void setCompany(String company) {
		this.company = company;
	}
}
