package chinsoft.entity;
public class Contrast implements java.io.Serializable {
	private static final long serialVersionUID = 8973729804682315200L;
	private Integer positionID;
	private String componentIDs;
	private String memo;
	private Integer colorCategoryID;
	
	public Integer getColorCategoryID() {
		return colorCategoryID;
	}

	public void setColorCategoryID(Integer colorCategoryID) {
		this.colorCategoryID = colorCategoryID;
	}

	public String getMemo() {
		return memo;
	}

	public void setMemo(String memo) {
		this.memo = memo;
	}

	public Integer getPositionID() {
		return this.positionID;
	}

	public void setPositionID(Integer positionID) {
		this.positionID = positionID;
	}
	
	public String getComponentIDs() {
		return componentIDs;
	}

	public void setComponentIDs(String componentIDs) {
		this.componentIDs = componentIDs;
	}
}