package chinsoft.entity;

public class Lapel {
	private Integer ID;
	private Integer buttonID;
	private Integer lapelID;
	private Integer widthID;
	public Integer getButtonID() {
		return buttonID;
	}
	public void setButtonID(Integer buttonID) {
		this.buttonID = buttonID;
	}
	public Integer getLapelID() {
		return lapelID;
	}
	public void setLapelID(Integer lapelID) {
		this.lapelID = lapelID;
	}
	public Integer getWidthID() {
		return widthID;
	}
	public void setWidthID(Integer widthID) {
		this.widthID = widthID;
	}
	public Integer getID() {
		return ID;
	}
	public void setID(Integer iD) {
		ID = iD;
	}
	
	
	
}
