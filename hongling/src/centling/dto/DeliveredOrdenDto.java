package centling.dto;


public class DeliveredOrdenDto implements java.io.Serializable{
	private static final long serialVersionUID = 7787966541165204028L;
	private Integer number;     // 序号
	private String ordenID;     // 订单ID
	private String clothingName;// 产品分类
	private String customerName;// 客户姓名
	private String fabricCode;  // 面料
	private String pubDate;     // 下单时间
	private String deliveryDate;// 发货时间
	private String dealDate;    // 交货日期
	private String chukudanId;  // 出库单号
	private String statusName;  //状态
	private String userordeNo;  //客户单号
	public DeliveredOrdenDto() {
		super();
	}
	public DeliveredOrdenDto(Integer number, String ordenID,
			String clothingName, String customerName, String fabricCode,
			String pubDate, String deliveryDate, String dealDate, String chukudanId,String statusName,String userordeNo) {
		super();
		this.number = number;
		this.ordenID = ordenID;
		this.clothingName = clothingName;
		this.customerName = customerName;
		this.fabricCode = fabricCode;
		this.pubDate = pubDate;
		this.deliveryDate = deliveryDate;
		this.dealDate = dealDate;
		this.chukudanId = chukudanId;
		this.statusName = statusName;
		this.setUserordeNo(userordeNo);
	}
	public Integer getNumber() {
		return number;
	}
	public void setNumber(Integer number) {
		this.number = number;
	}
	public String getOrdenID() {
		return ordenID;
	}
	public void setOrdenID(String ordenID) {
		this.ordenID = ordenID;
	}
	public String getClothingName() {
		return clothingName;
	}
	public void setClothingName(String clothingName) {
		this.clothingName = clothingName;
	}
	public String getCustomerName() {
		return customerName;
	}
	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}
	public String getFabricCode() {
		return fabricCode;
	}
	public void setFabricCode(String fabricCode) {
		this.fabricCode = fabricCode;
	}
	public String getPubDate() {
		return pubDate;
	}
	public void setPubDate(String pubDate) {
		this.pubDate = pubDate;
	}
	public String getDeliveryDate() {
		return deliveryDate;
	}
	public void setDeliveryDate(String deliveryDate) {
		this.deliveryDate = deliveryDate;
	}
	public String getDealDate() {
		return dealDate;
	}
	public void setDealDate(String dealDate) {
		this.dealDate = dealDate;
	}
	public String getChukudanId() {
		return chukudanId;
	}
	public void setChukudanId(String chukudanId) {
		this.chukudanId = chukudanId;
	}
	public String getStatusName() {
		return statusName;
	}
	public void setStatusName(String statusName) {
		this.statusName = statusName;
	}
	public String getUserordeNo() {
		return userordeNo;
	}
	public void setUserordeNo(String userordeNo) {
		this.userordeNo = userordeNo;
	}
}
