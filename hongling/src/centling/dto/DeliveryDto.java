package centling.dto;

import java.io.Serializable;
import java.util.Date;

public class DeliveryDto implements Serializable{
	private static final long serialVersionUID = 5296939420939276717L;
	
	/**
	 * 序号
	 */
	private int number;
	/**
	 * 发货单号
	 */
	private String ID;
	/**
	 * 用户名
	 */
	private String username;
	/**
	 * 用户简称
	 */
	private String name;
	/**
	 * 发货地址
	 */
	private String deliveryAddress;
	/**
	 * 生成时间
	 */
	private Date applyDate;  
	/**
	 * 发货时间
	 */
	private Date deliveryDate;
	/**
	 * 运单号
	 */
	private String yundanId;
	
	/**
	 * 状态ID
	 */
	private String statusId;
	
	/**
	 * 快递公司编号
	 */
	private String expressComId;
	
	/**
	 * 快递公司名称
	 */
	private String expressComName;
	
	/**
	 * 状态名称
	 */
	private String statusName;
	
	int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDeliveryAddress() {
		return deliveryAddress;
	}
	public void setDeliveryAddress(String deliveryAddress) {
		this.deliveryAddress = deliveryAddress;
	}
	public Date getApplyDate() {
		return applyDate;
	}
	public void setApplyDate(Date applyDate) {
		this.applyDate = applyDate;
	}
	public Date getDeliveryDate() {
		return deliveryDate;
	}
	public void setDeliveryDate(Date deliveryDate) {
		this.deliveryDate = deliveryDate;
	}
	public String getYundanId() {
		return yundanId;
	}
	public void setYundanId(String yundanId) {
		this.yundanId = yundanId;
	}
	public String getStatusId() {
		return statusId;
	}
	public void setStatusId(String statusId) {
		this.statusId = statusId;
	}
	public String getExpressComId() {
		return expressComId;
	}
	public void setExpressComId(String expressComId) {
		this.expressComId = expressComId;
	}
	public String getExpressComName() {
		return expressComName;
	}
	public void setExpressComName(String expressComName) {
		this.expressComName = expressComName;
	}
	public String getStatusName() {
		return statusName;
	}
	public void setStatusName(String statusName) {
		this.statusName = statusName;
	}
}