package centling.dto;

public class DealDto implements java.io.Serializable{
	private static final long serialVersionUID = -6956936297266721450L;
	private String dealDate;         // 交易日期
	private String companyName;      // 公司名称
	private String companyShortName; // 客户简称
	private String username;         // 用户名
	private String dealItemName;     // 交易项目
	private String moneySignName;    // 币种
	private String accountIn;        // 记账金额（收入）
	private String accountOut;       // 记账金额（支出）
	private String ordenId;          // 订单号
	private String yundanId;         // 运单号
	private String num;              // 余额
	private String memo;             // 备注

	public DealDto() {
		super();
	}
	public DealDto(String dealDate, String companyName,
			String companyShortName, String username, String dealItemName,
			String moneySignName, String accountIn, String accountOut,
			String ordenId, String yundanId, String num, String memo) {
		super();
		this.dealDate = dealDate;
		this.companyName = companyName;
		this.companyShortName = companyShortName;
		this.username = username;
		this.dealItemName = dealItemName;
		this.moneySignName = moneySignName;
		this.accountIn = accountIn;
		this.accountOut = accountOut;
		this.ordenId = ordenId;
		this.yundanId = yundanId;
		this.num = num;
		this.memo = memo;
	}
	public String getDealDate() {
		return dealDate;
	}
	public void setDealDate(String dealDate) {
		this.dealDate = dealDate;
	}
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public String getCompanyShortName() {
		return companyShortName;
	}
	public void setCompanyShortName(String companyShortName) {
		this.companyShortName = companyShortName;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	
	public String getDealItemName() {
		return dealItemName;
	}
	public String getMemo() {
		return memo;
	}
	public void setMemo(String memo) {
		this.memo = memo;
	}
	public String getMoneySignName() {
		return moneySignName;
	}
	public void setMoneySignName(String moneySignName) {
		this.moneySignName = moneySignName;
	}
	public String getAccountIn() {
		return accountIn;
	}
	public void setAccountIn(String accountIn) {
		this.accountIn = accountIn;
	}
	public String getAccountOut() {
		return accountOut;
	}
	public void setAccountOut(String accountOut) {
		this.accountOut = accountOut;
	}
	public String getOrdenId() {
		return ordenId;
	}
	public void setOrdenId(String ordenId) {
		this.ordenId = ordenId;
	}
	public String getYundanId() {
		return yundanId;
	}
	public void setYundanId(String yundanId) {
		this.yundanId = yundanId;
	}
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public void setDealItemName(String dealItemName) {
		this.dealItemName = dealItemName;
	}
}
