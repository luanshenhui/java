package centling.dto;

public class StatementDto {
	/**
	 * 交易项目ID
	 */
	private String dealItemId;
	/**
	 * 数量
	 */
	private String qyt;
	/**
	 * 金额
	 */
	private String amount;
	
	/**
	 * 收入、支出标识
	 */
	private Integer ioFlag;
	
	public String getDealItemId() {
		return dealItemId;
	}
	public void setDealItemId(String dealItemId) {
		this.dealItemId = dealItemId;
	}
	public String getQyt() {
		return qyt;
	}
	public void setQyt(String qyt) {
		this.qyt = qyt;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public Integer getIoFlag() {
		return ioFlag;
	}
	public void setIoFlag(Integer ioFlag) {
		this.ioFlag = ioFlag;
	}
}