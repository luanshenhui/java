package com.dpn.dpows.standard.model;

public class WorkDeclareGoods {
	
	private String declareGoodsId;
	
	private String status;
	
	private String verifyUserId;
	
	private String verifyOpinion;

	public String getDeclareGoodsId() {
		return declareGoodsId;
	}

	public void setDeclareGoodsId(String declareGoodsId) {
		this.declareGoodsId = declareGoodsId;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getVerifyUserId() {
		return verifyUserId;
	}

	public void setVerifyUserId(String verifyUserId) {
		this.verifyUserId = verifyUserId;
	}

	public String getVerifyOpinion() {
		return verifyOpinion;
	}

	public void setVerifyOpinion(String verifyOpinion) {
		this.verifyOpinion = verifyOpinion;
	}
	
}
