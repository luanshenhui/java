package com.dpn.ciqqlc.standard.model;

import java.util.Date;

/**
 * 备案申请附件表
 * @author Administrator
 *
 */
public class EfpeApplyFileDTO {  
	
	private String applyid;//申请ID
	
	private String accessoryid;//附件ID
	
	private String filename;//附件名称
	
	private Date startdate;//申请时间
	private String  startdateStr;//申请时间
	
	private String filetype;//附件类型
	
	private String path;//存储路径
	
	private String attname;//模板分类名称

	public String getApplyid() {
		return applyid;
	}

	public void setApplyid(String applyid) {
		this.applyid = applyid;
	}

	public String getAccessoryid() {
		return accessoryid;
	}

	public void setAccessoryid(String accessoryid) {
		this.accessoryid = accessoryid;
	}

	public String getFilename() {
		return filename;
	}

	public void setFilename(String filename) {
		this.filename = filename;
	}

	public Date getStartdate() {
		return startdate;
	}

	public void setStartdate(Date startdate) {
		this.startdate = startdate;
	}

	public String getFiletype() {
		return filetype;
	}

	public void setFiletype(String filetype) {
		this.filetype = filetype;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public String getAttname() {
		return attname;
	}

	public void setAttname(String attname) {
		this.attname = attname;
	}

	public String getStartdateStr() {
		return startdateStr;
	}

	public void setStartdateStr(String startdateStr) {
		this.startdateStr = startdateStr;
	}

}
