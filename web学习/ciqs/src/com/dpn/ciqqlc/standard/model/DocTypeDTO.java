package com.dpn.ciqqlc.standard.model;

import java.util.Date;

public class DocTypeDTO {

	private String doc_id;
	private String doc_type;
	private String dec_user;
	private Date dec_date;
	private String group_id;
	private String name;
	private String dec_date_str;
	public String getDoc_id() {
		return doc_id;
	}
	public void setDoc_id(String doc_id) {
		this.doc_id = doc_id;
	}
	public String getDoc_type() {
		return doc_type;
	}
	public void setDoc_type(String doc_type) {
		this.doc_type = doc_type;
	}
	public String getDec_user() {
		return dec_user;
	}
	public void setDec_user(String dec_user) {
		this.dec_user = dec_user;
	}
	public Date getDec_date() {
		return dec_date;
	}
	public void setDec_date(Date dec_date) {
		this.dec_date = dec_date;
	}
	public String getGroup_id() {
		return group_id;
	}
	public void setGroup_id(String group_id) {
		this.group_id = group_id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDec_date_str() {
		return dec_date_str;
	}
	public void setDec_date_str(String dec_date_str) {
		this.dec_date_str = dec_date_str;
	}
	
}
