package com.dpn.ciqqlc.standard.model;

import java.util.Date;
/**
 * 
 * table QLC_TRAV_CARRY_OBJ 
 * @author liuchao
 *
 */
public class TravCarryObjDTO {
	/**
	 * 主键 UUID
	 */
	private String id;
	/**
	 * 护照号码
	 */
	private String cardNo;
	/**
	 * 业务单号
	 */
	private String prodMainId;
	/**
	 * 口岸局
	 */
	private String portOrgCode;
	/**
	 * 科室
	 */
	private String portDeptCode;
	/**
	 * 创建人
	 */
	private String createUser;
	/**
	 * 创建时间
	 */
	private Date createDate;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCardNo() {
		return cardNo;
	}
	public void setCardNo(String cardNo) {
		this.cardNo = cardNo;
	}
	public String getProdMainId() {
		return prodMainId;
	}
	public void setProdMainId(String prodMainId) {
		this.prodMainId = prodMainId;
	}
	public String getPortOrgCode() {
		return portOrgCode;
	}
	public void setPortOrgCode(String portOrgCode) {
		this.portOrgCode = portOrgCode;
	}
	public String getPortDeptCode() {
		return portDeptCode;
	}
	public void setPortDeptCode(String portDeptCode) {
		this.portDeptCode = portDeptCode;
	}
	public String getCreateUser() {
		return createUser;
	}
	public void setCreateUser(String createUser) {
		this.createUser = createUser;
	}
	public Date getCreateDate() {
		return createDate;
	}
	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}

}
