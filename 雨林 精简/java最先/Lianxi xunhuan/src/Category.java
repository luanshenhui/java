
public class Category {
private int i;
private String string;
	public Category(int id, String name) {
		this.i=id;
		this.string=name;
	}
	public int getI() {
		return i;
	}
	public void setI(int i) {
		this.i = i;
	}
	public String getString() {
		return string;
	}
	public void setString(String string) {
		this.string = string;
	}
	@Override
	public String toString() {
		return "s";
	}

}
